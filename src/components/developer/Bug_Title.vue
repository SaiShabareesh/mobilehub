<script>
import uuidV4 from "uuid/v4";
import { mapState } from "vuex";
import CreateBugs from "../../mutations/CreateBugs";
import ListBugs from "../../queries/ListBugs";

export default {
  name: "Bugfix_Market",
  computed: {
    ...mapState({
      user: state => state.auth.user
    })
  },
  /* create bug strat from here */
  methods: {
    createBugs() {
      const error = this.error;
      const code = this.code;
      const username = this.user.username;
      if (error === "" || code === "") {
        alert("please enter all fields");
        return;
      }
      this.error = "";
      this.code = "";
      const id = uuidV4();
      const bugfix = {
        username: username,
        error: error,
        code: code,
        id
      };
      this.$apollo
        .mutate({
          mutation: CreateBugs,
          variables: bugfix,
          update: (store, { data: { createBugs } }) => {
            const data = store.readQuery({ query: ListBugs });
            data.listBugs.items.push(createBugs);
            store.writeQuery({ query: ListBugs, data });
          },
          optimisticResponse: {
            __typename: "Mutation",
            createBugs: {
              __typename: "Bugs",
              ...bugfix
            }
          }
        })
        .then(data => console.log(data))
        .catch(error => console.error("error!!!: ", error));
    }
  },
  data() {
    return {
      error: "",
      code: "",
      username: "",
      bugs: []
    };
  },

  /* To get list bugs code start from here */
  apollo: {
    bugs: {
      query: () => ListBugs,
      update: data => data.listBugs.items
    }
   
  }
};
